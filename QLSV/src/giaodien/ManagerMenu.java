    public static List<String> pastCSVLine(String csvLine)
    {
        List<String> result = new ArrayList<String>();
	if (csvLine != null) {
            String[] splitData = csvLine.split(",");
            for (int i = 0; i < splitData.length; i++)
            {
               result.add(splitData[i]);
            }
        }
	return result;
    }

